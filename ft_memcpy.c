#include "libft.h"

void	*ft_memcpy(void *str1, const void *str2, size_t n)
{
	char	*cs1;
	char	*cs2;

	cs1 = ((char*)str1);
	cs2 = ((char*)str2);
	while (0 < n)
	{
		*cs1++ = *cs2++;
		n--;
	}
	return (str1);
}
