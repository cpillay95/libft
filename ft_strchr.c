#include <string.h>

char	*ft_strchr(const char *str, int c)
{
	int i;

	i = 0;
	while (*str != c)
	{
		if (str[i] == '\0')
		{
			return (NULL);
		}
		str++;
	}
	return ((char*)&str[i]);
}
